#include<bits/stdc++.h>
using namespace std;


int main()
{
    int n,m;
    // cin>>n>>m;
    n=3;m=4;
    int m1[n][m] = {
                    {1,2,3,4},     //00 01 02 03
                    {5,6,7,8},     //10 11 12 13
                    {9,10,11,12}   //20 21 22 23
                    };
    // for(int i=0;i<n;i++){
    //     for(int j=0;j<m;j++){
    //         cin>>m1[i][j];
    //     }
    // }
    int p,q;
    // cin>>p>>q;
    p=4;q=3;
    int m2[p][q] = {
                    {1,2,3},      //00 01 02
                    {4,5,6},      //10 11 12
                    {7,8,9},      //20 21 22
                    {10,11,12}    //30 31 32
                    };
    // for(int i=0;i<p;i++){
    //     for(int j=0;j<q;j++){
    //         cin>>m2[i][j];
    //     }
    // }

//  (00*00 + 01*10 + 02*20 + 03*30) (00*01 + 01*11 + 02*21 + 03*31) (00*02 + 01*12 + 02*22 + 03*32) 
//  (10*00 + 11*10 + 12*20 + 13*30) (10*01 + 11*11 + 12*21 + 13*31) (10*02 + 11*12 + 12*22 + 13*32)
//  (20*00 + 21*10 + 22*20 + 23*30) (20*01 + 21*11 + 22*21 + 23*31) (20*02 + 21*12 + 22*22 + 23*32)

    if(m==p){
        int mul[n][q];
        for(int i=0;i<n;i++){
            for(int j=0;j<q;j++){
                mul[i][j] = 0;
            }
        }

        for(int i=0;i<n;i++){
            for(int j=0;j<q;j++){
                 for(int k=0;k<m;k++){
                    mul[i][j] += m1[i][k]*m2[k][j]; 
                 }
            }
        }
    for(int i=0;i<n;i++){
        for(int j=0;j<q;j++){
            cout<<mul[i][j]<<" ";
        }
        cout<<endl;
    }
    }
    return 0;
}